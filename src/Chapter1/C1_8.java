/**
 * Program to put in multiple variables and compute the circumference and radius
 * of a circle
 *
 * @author Kieran Sowerby
 */
public class C1_8 {

    /**
     * Main Method
     *
     * @param args arguments from cmd line prompt
     */
    public static void main(String[] args) {
        double int1 = 5.5, int2 = 2, int3 = 3.14;
        double result1 = int1 * int2 * int3;
        double int4 = 5.5, int5 = 5.5, int6 = 3.14;
        double result2 = int4 * int5 * int6;
        System.out.println(result1);
        System.out.println(result2);
    }
}
